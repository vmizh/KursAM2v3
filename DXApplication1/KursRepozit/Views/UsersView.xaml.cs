using System.Windows;
using System.Windows.Controls;
using DevExpress.Xpf.Editors.Settings;
using DevExpress.Xpf.Grid;
using KursRepozit.ViewModels;

namespace KursRepozit.Views
{
    /// <summary>
    ///     Interaction logic for UsersView.xaml
    /// </summary>
    public partial class UsersView
    {
        public UsersView()
        {
            InitializeComponent();
        }

        private void GridControlUsers_OnAutoGeneratingColumn(object sender, AutoGeneratingColumnEventArgs e)
        {
            e.Column.Name = e.Column.FieldName;
            switch (e.Column.Name)
            {
                case nameof(UserViewModel.AvatarSource):
                    e.Column.EditSettings = new PopupImageEditSettings
                    {
                        PopupMaxWidth = 350,
                        PopupMaxHeight = 350
                    };
                    break;
                case nameof(UserViewModel.IsAdmin):
                    e.Column.EditSettings = new CheckEditSettings
                    {
                        ClickMode = ClickMode.Release,
                    };
                    break;
            }
        }

        private void GridControlUsers_OnAutoGeneratedColumns(object sender, RoutedEventArgs e)
        {

        }

        private void GridControlDS_OnAutoGeneratingColumn(object sender, AutoGeneratingColumnEventArgs e)
        {
            e.Column.Name = e.Column.FieldName;
            if(e.Column.Name != "IsSelected") 
                e.Column.ReadOnly = true;
            switch (e.Column.Name)
            {
                case nameof(DataSourceViewModel.Color):
                    e.Column.EditSettings = new PopupColorEditSettings();
                    break;
            }
        }

        private void GridControlDS_OnAutoGeneratedColumns(object sender, RoutedEventArgs e)
        {
            
        }
    }
}
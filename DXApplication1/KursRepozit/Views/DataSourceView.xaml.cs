using System.Windows;
using DevExpress.Xpf.Editors.Settings;
using DevExpress.Xpf.Grid;
using KursRepozit.ViewModels;

namespace KursRepozit.Views
{
    /// <summary>
    ///     Interaction logic for DataSourceView.xaml
    /// </summary>
    public partial class DataSourceView
    {
        public DataSourceView()
        {
            InitializeComponent();
        }

        private void GridControlDataSource_OnAutoGeneratingColumn(object sender, AutoGeneratingColumnEventArgs e)
        {
            e.Column.Name = e.Column.FieldName;
            switch (e.Column.Name)
            {
                case nameof(DataSourceViewModel.Color):
                    e.Column.EditSettings = new PopupColorEditSettings();
                    break;
            }
        }

        private void GridControlDataSource_OnAutoGeneratedColumns(object sender, RoutedEventArgs e)
        {
        }
    }
}
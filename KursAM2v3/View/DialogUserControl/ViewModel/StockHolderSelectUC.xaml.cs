    /// <summary>
    ///     Interaction logic for StockHolderSelectUC.xaml
    /// </summary>
    public partial class StockHolderSelectUC : IDataUserControl
    {
        public StockHolderSelectUC()
        {
            InitializeComponent();
            LayoutManager = new LayoutManager.LayoutManager(GetType().Name, gridControlSearch);
            LayoutControl = gridControlSearch;
            Loaded += StockHolderSelectUC_Loaded;
            Unloaded += StockHolderSelectUC_Unloaded;
        }

        public LayoutManagerBase LayoutManager { get; set; }
        public DependencyObject LayoutControl { get; }

        private void StockHolderSelectUC_Unloaded(object sender, RoutedEventArgs e)
        {
            LayoutManager.Save();
        }

        private void StockHolderSelectUC_Loaded(object sender, RoutedEventArgs e)
        {
            LayoutManager.Load();
        }

        private void GridControlSearch_OnAutoGeneratingColumn(object sender, AutoGeneratingColumnEventArgs e)
        {
            e.Column.Name = e.Column.FieldName;
            e.Column.ReadOnly = true;
        }


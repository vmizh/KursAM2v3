using System.ComponentModel;
using System.Windows;
using DevExpress.Xpf.Editors.Settings;
using DevExpress.Xpf.Grid;
using LayoutManager;

namespace KursAM2.View.Logistiks
{
    /// <summary>
    ///     Interaction logic for WarehouseStorageOrderView.xaml
    /// </summary>
    public partial class WarehouseStorageOrderView : ILayout
    {
        public WarehouseStorageOrderView()
        {
            InitializeComponent();
            LayoutManager = new LayoutManager.LayoutManager(GetType().Name, this, mainLayoutControl);
            Loaded += WerhouseOrder_Loaded;
            Closing += WerhouseOrder_Closing;
        }

        public LayoutManagerBase LayoutManager { get; set; }
        public string LayoutManagerName { get; set; }

        private void WerhouseOrder_Closing(object sender, CancelEventArgs e)
        {
            LayoutManager.Save();
        }

        private void WerhouseOrder_Loaded(object sender, RoutedEventArgs e)
        {
            LayoutManager.Load();
        }

        private void BankGridControl_OnAutoGeneratedColumns(object sender, RoutedEventArgs e)
        {
            gridRows.TotalSummary.Clear();
            foreach (var col in gridRows.Columns)
                if (col.FieldType == typeof(decimal) || col.FieldType == typeof(decimal?) ||
                    col.FieldType == typeof(int?) || col.FieldType == typeof(int) ||
                    col.FieldType == typeof(float?) || col.FieldType == typeof(float) ||
                    col.FieldType == typeof(double?) || col.FieldType == typeof(double))
                    if (col.FieldName == "DDT_KOL_PRIHOD")
                        col.EditSettings = new CalcEditSettings
                        {
                            AllowDefaultButton = false,
                            DisplayFormat = "n3",
                            MaskUseAsDisplayFormat = true
                        };
                    else
                        col.EditSettings = new CalcEditSettings
                        {
                            AllowDefaultButton = false,
                            DisplayFormat = "n2",
                            MaskUseAsDisplayFormat = true
                        };
        }

        private void BankGridControl_OnAutoGeneratingColumn(object sender, AutoGeneratingColumnEventArgs e)
        {
            e.Column.Name = e.Column.FieldName;
        }
    }
}
using System.Windows;
using DevExpress.Xpf.Core;
using DevExpress.Xpf.Editors.Settings;
using DevExpress.Xpf.Grid;
using Helper;
using KursAM2.ViewModel.Finance.DistributeNaklad;
using LayoutManager;

namespace KursAM2.View.Finance.DistributeNaklad
{
    /// <summary>
    ///     Interaction logic for DistributeNakladSearchView.xaml
    /// </summary>
    public partial class DistributeNakladSearchView : ILayout
    {
        public DistributeNakladSearchView(Window parentForm)
        {
            InitializeComponent(); 
            
            DataContext = new DistributeNakladSearchViewModel(parentForm);
        }

        private void KursBaseSearchWindow_OnAutoGeneratedColumns(object sender, RoutedEventArgs e)
        {
        }

        private void KursBaseSearchWindow_OnAutoGeneratingColumn(object sender, AutoGeneratingColumnEventArgs e)
        {
            e.Column.Name = e.Column.FieldName;
            if (KursGridControlHelper.ColumnFieldTypeCheckDecimal(e.Column.FieldType))
                e.Column.EditSettings = new CalcEditSettings
                {
                    DisplayFormat = "n2",
                    Name = e.Column.FieldName + "Calc"
                };
            switch (e.Column.FieldName)
            {
                case "State":
                    e.Column.Visible = false;
                    break;
            }
        }

        public LayoutManager.LayoutManager LayoutManager { get; set; }
        public string LayoutManagerName { get; set; }
        public void SaveLayout()
        {
            LayoutManager.Save();
        }
    }
}

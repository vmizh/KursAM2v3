            //gridFacts.Height = columnsInfo.Height;
            //gridFacts.Width = columnsInfo.Width;
            foreach (var col in gridFacts.Columns) gridFactsLayout.AutoGeneratedColumnSetProperties(columnsInfo.ColumnsInfo, col);
        }

